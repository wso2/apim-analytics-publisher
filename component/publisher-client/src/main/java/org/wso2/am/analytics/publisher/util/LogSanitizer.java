package org.wso2.am.analytics.publisher.util;

/**
 * Utility class for sanitizing log messages by removing carriage return and newline characters.
 */
public class LogSanitizer {
    public static String sanitize(String input) {
        return input != null ? input.replaceAll("[\r\n]", "") : null;
    }
}
